package ex_04_Operators;

public class Lab16_IQ_Concat_Plus {
    static void main() {
        int a = 10;
        int b = 20;
        System.out.println(a + b);

        //+

        String first_name="Raghu";
        String second_name="Ram";
        System.out.println(first_name+second_name);
        System.out.println(first_name+(a+b));
        System.out.println(a+b+second_name);

        // + -> Behave differently with the data type
    }

}
