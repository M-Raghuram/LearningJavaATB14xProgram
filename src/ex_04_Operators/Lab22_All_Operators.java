package ex_04_Operators;

public class Lab22_All_Operators {
    static void main() {
        int a = 10;
        int b = 20;

        System.out.println(a+b);

        System.out.println(a>b);

        System.out.println(a>=b);

        System.out.println(a==b);

        System.out.println(a!=b);
    }

}
